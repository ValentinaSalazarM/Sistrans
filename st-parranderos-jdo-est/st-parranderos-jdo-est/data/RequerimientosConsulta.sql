--RFC3 - MOSTRAR EL ÍNDICE DE AFORO DEL TIPO DE LOCAL COMERCIAL EN UNA FECHA
SELECT TIPO, NUM_VISITAS, SUM(AREA.AFORO) AS AFORO_TOTAL
FROM LOCALCOMERCIAL
JOIN
    (
        SELECT TIPOLOCAL.ID, TIPOLOCAL.TIPO, COUNT(*) AS NUM_VISITAS
        FROM REGISTRANCARNET JOIN LECTOR
        ON REGISTRANCARNET.IDLECTOR = LECTOR.ID
        JOIN LOCALCOMERCIAL
        ON LECTOR.IDLOCALCOMERCIAL = LOCALCOMERCIAL.IDENTIFICADOR
        JOIN TIPOLOCAL
        ON LOCALCOMERCIAL.TIPOLOCAL = TIPOLOCAL.ID
        WHERE FECHA =  TO_DATE('2019/07/19','yyyy/mm/dd') AND TIPOLOCAL.TIPO = 'Restaurante'
        GROUP BY TIPOLOCAL.ID, TIPOLOCAL.TIPO
    ) AUX
ON LOCALCOMERCIAL.TIPOLOCAL = AUX.ID
JOIN AREA
ON LOCALCOMERCIAL.AREA = AREA.ID
GROUP BY TIPO, NUM_VISITAS;

